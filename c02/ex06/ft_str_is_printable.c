int		ft_str_is_printable(char *str)
{
	int	count;
	int i;

	count = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= 32 && str[i] <= 126)))
			count = 0;
		i++;
	}
	return (count);
}
