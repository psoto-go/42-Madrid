int		ft_str_is_numeric(char *str)
{
	int	count;
	int i;

	count = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 48 && str[i] <= 57))
			count = 0;
		i++;
	}
	return (count);
}
