#include <unistd.h>

void	cuatros3(int tab[4][4], int *srw, int *irw)
{
	int		i;
	char	c;

	i = 0;
	c = '0';
	while (i < 4)
	{
		if (srw[i] == 2 && irw[i] == 3)
		{
			tab[1][i] = 4;
			c = tab[1][i];
			write(1, &c, 1);
		}
		if (srw[i] == 3 && irw[i] == 2)
		{
			tab[2][i] = 4;
			c = tab[2][i];
			write(1, &c, 1);
		}
		i++;
	}
}
