int		ft_str_is_alpha(char *str)
{
	int	count;
	int i;

	count = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (!((str[i] >= 65 && str[i] <= 90) ||
					(str[i] >= 97 && str[i] <= 122)))
			count = 0;
		i++;
	}
	return (count);
}
