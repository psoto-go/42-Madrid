#include <unistd.h>

void	cuatros1(int tab[4][4], int *srw, int *irw)
{
	int		i;
	char	c;

	i = 0;
	c = '0';
	while (i < 4)
	{
		if (srw[i] == 1)
		{
			tab[0][i] = 4;
			c = tab[0][i] + '0';
			write(1, &c, 1);
		}
		if (irw[i] == 1)
		{
			tab[3][i] = 4;
			c = tab[3][i] + '0';
			write(1, &c, 1);
		}
		i++;
	}
}
