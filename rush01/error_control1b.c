#include <unistd.h>

int		error_control1b(char **argv)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (i < 31)
	{
		if (((i % 2) == 0) && ((argv[1][i] < 49) || (argv[1][i] > 52)))
		{
			write(1, "Error\n", 6);
			n = 0;
		}
		else if (((i % 2) != 0) && (argv[1][i] != 32))
		{
			write(1, "Error\n", 6);
			n = 0;
		}
		i++;
	}
	return (n);
}
