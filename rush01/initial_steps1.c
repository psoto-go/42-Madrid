void	initial_steps1(char **argv, int tab[4][4], int *suprw)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 4)
	{
		while (j < 4)
		{
			tab[i][j] = 0;
			j++;
		}
		j = 0;
		i++;
	}
	i = 0;
	j = 0;
	while (i < 8)
	{
		suprw[j] = argv[1][i];
		i = i + 2;
		j++;
	}
}
